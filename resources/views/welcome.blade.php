        document.addEventListener("DOMContentLoaded", function() {
            document.querySelectorAll(".updateProductButton").forEach((item) => {
                item.addEventListener("click", (event) => {
                    const id = item.getAttribute("data-id");
                    const menu = item.getAttribute("data-menu");
                    const harga = item.getAttribute("data-harga");
                    const stok = item.getAttribute("data-stok");
                    const kategori = item.getAttribute("data-kategori");
                    const deskripsi = item.getAttribute("data-deskripsi");
                    const gambar = item.getAttribute("data-gambar");

                    console.log("Update Menu:", id, menu, harga, stok, kategori, deskripsi);

                    // Isi form dengan data dari tombol edit
                    document.getElementById("update-menu-id").value = id;
                    document.getElementById("update-menu").value = menu;
                    document.getElementById("update-harga").value = harga;
                    document.getElementById("update-stok").value = stok;
                    document.getElementById("update-deskripsi").value = deskripsi;

                    // Pilih kategori yang sesuai
                    const updateKategori = document.getElementById("updateKategori");
                    for (let option of updateKategori.options) {
                        if (option.value == kategori) {
                            option.selected = true;
                            break;
                        }
                    }

                    // Ubah action form dengan ID menu
                    document.getElementById("updateMenuForm").action = `/menu/${id}`;

                    // Set gambar lama jika ada
                    if (gambar) {
                        const previewContainer = document.getElementById("preview-container");
                        const preview = document.getElementById("gambar-preview");
                        preview.src = gambar;
                        previewContainer.classList.remove("hidden"); // Tampilkan preview
                    }
                });
            });
        });

